public class OperadoresRelacionais {
    public static void main(String[] args)
    {
    // operadores relacionais, de comparação.
     int    valor1 = 1;
     int    valor2 = 2;
     
     System.out.println("valor1 == valor2: " + (valor1 == valor2) );
     System.out.println("valor1 != valor2: " + (valor1 != valor2));
     System.out.println("valor1 < valor2: "  + (valor1 < valor2));
     System.out.println("valor1 > valor2: " + (valor1 > valor2));
     System.out.println("valor1 <= valor2: " + (valor1 <= valor2));
     System.out.println("valor1 >= valor2: " + (valor1 >= valor2));
     
     // sempre retorna um boolean
       
    }
}
